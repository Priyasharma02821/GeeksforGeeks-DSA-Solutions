class Solution {
  public:
    vector<int> uniqueId(vector<int>& arr) {
        unordered_set<int> seen;
    vector<int> result;

    for (int id : arr) {
        if (seen.find(id) == seen.end()) {
            result.push_back(id);
            seen.insert(id);
        }
    }

    return result;
    }
};

